
#올림픽 오륜기 작성
import turtle as t

t.shape('turtle')
t.speed(999)
t.pensize(5)


t.circle(100)

t.up()
t.forward(240)
t.down()
t.pencolor("red")
t.circle(100)


t.up()
t.backward(480)
t.down()
t.pencolor("blue")
t.circle(100)

t.up()
t.right(90)
t.forward(120)
t.left(90)
t.forward(120)
t.down()
t.pencolor('yellow')
t.circle(100)


t.up()
t.forward(240)
t.down()
t.pencolor("green")
t.circle(100)


t.done()